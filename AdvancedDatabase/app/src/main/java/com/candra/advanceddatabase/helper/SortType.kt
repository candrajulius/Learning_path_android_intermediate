package com.candra.advanceddatabase.helper

enum class SortType {
    ASCENDING,
    DESCENDING,
    RANDOM
}
/*
Variabel-variabel di atas digunakan untuk menentukan tipe urutan. ASCENDING digunakan untuk mengurutkan data berdasarkan abjad A ke Z, sebaliknya DESCENDING digunakan untuk mengurutkan data berdasarkan abjad Z ke A. Sedangkan RANDOM untuk mengurutkan secara acak.
 */